    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        
        try{
            int id = (Integer) this.ChampComboBox.getSelectedItem();
            String name = this.NameInput.getText();
            int year = Integer.parseInt(this.YearInput.getText());
            
            ChampionshipController champC = new ChampionshipController();
            champC.editChampionship(id, name, year);
            
            initList();
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error with DB {"+e.toString()+"}");
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro {"+e.toString()+"}");
        }
    }//GEN-LAST:event_editBtnActionPerformed

