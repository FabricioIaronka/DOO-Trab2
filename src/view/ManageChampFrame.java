    private void initList(){        
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setNumRows(0);
        
            DefaultComboBoxModel dcm = (DefaultComboBoxModel) ChampComboBox.getModel();
            dcm.removeAllElements();
        
            DefaultComboBoxModel dcmd = (DefaultComboBoxModel) DeleteChampComboBox.getModel();
            dcmd.removeAllElements();
            
            ChampionshipController champC = new ChampionshipController();
            this.champList = champC.listChampioship();
            
            for(int i = 0; i < this.champList.size(); i++){
                ChampionshipModel champ = this.champList.get(i);
                
                dtm.addRow(new Object[]{
                    champ.getId(),
                    champ.getName(),
                    champ.getYear(),}
                );
                
                dcm.addElement(champ.getId());
                dcmd.addElement(champ.getId());
            }
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error with DB {"+e.toString()+"}");
        }
        catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro {"+e.toString()+"}");
        }
        
    }

