    private void ChampComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ChampComboBoxActionPerformed
        int index = this.ChampComboBox.getSelectedIndex();
        
        if (this.champList != null && index >= 0) {
        ChampionshipModel champ = this.champList.get(index);
        this.NameInput.setText(champ.getName());
        this.YearInput.setText(champ.getYear()+"");
        }
        
    }//GEN-LAST:event_ChampComboBoxActionPerformed

